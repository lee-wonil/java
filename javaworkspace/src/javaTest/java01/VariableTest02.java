package javaTest.java01;
//byte, short, int, long 타입 사용 예제

public class VariableTest02 {
	public static void main(String[] args) {
        byte byteVar = 10; //byte타입의 변수 byteVar선언
        short shortVar = 128; //short타입의 변수 shortVar선언
        int intVar = 32768; //int타입의 변수intVar선언
        long longVar = 2147243648; //long타입의 변수 longVar선언
        
        //변수들의 내용 출력
        System.out.println("byteVar = " + byteVar);
        System.out.println("shortVar = " + shortVar);
        System.out.println("intVar = " + intVar);
        System.out.println("longVar = " + longVar);
	}
}